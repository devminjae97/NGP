using System.Collections;
using System.Collections.Generic;
using UnityEngine;

public class MasterAsset : MonoBehaviour
{
    public static MasterAsset instance;

    private Camera camera1; // 첫 번째 카메라
    private Camera camera2; // 두 번째 카메라

    private void Awake()
    {
        if (instance == null)
        {
            instance = this;
            camera1 = GameObject.Find("Camera1").GetComponent<Camera>();
            camera2 = GameObject.Find("Camera2").GetComponent<Camera>();
            camera1.enabled = true;
            camera2.enabled = true;
            SetCameraRect();
        }
    }
    public void SetCameraRect()
    {
        camera1.rect = new Rect(0f, 0.5f, 1f, 0.5f); // 상단 영역
        camera2.rect = new Rect(0f, 0f, 1f, 0.5f);   // 하단 영역
    }
}
